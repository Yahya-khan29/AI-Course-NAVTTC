#This program convert temperature in celcius to farenheit

#initializing temperature in celcius
Temp_Celcius = 37.5

#Converting in farenheit
Temp_Farenheit = (Temp_Celcius * 1.8) + 32

#Dislaying the result
print("%0.1f Degree Celcius is equal to %0.1f Degree Farenheit"%(Temp_Celcius,Temp_Farenheit))
